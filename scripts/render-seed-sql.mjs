import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { execFileSync } from "node:child_process";

const [inputArg = "config/agent-setup.local.json", outputArg = "cloudflare/agent.seed.local.sql"] = process.argv
  .slice(2)
  .filter((arg) => arg !== "--");
const inputPath = resolve(inputArg);
const outputPath = resolve(outputArg);

execFileSync(process.execPath, ["scripts/validate-agent-setup.mjs", inputArg], { stdio: "inherit" });

const config = JSON.parse(readFileSync(inputPath, "utf8"));
const filterPresets = loadFilterPresets();
const now = Date.now();

const sourceRows = array(config.sources).map((source) => normalizeSource(source));
const collectionRows = array(config.collections).map((collection) => normalizeCollection(collection));
const templateRows = array(config.templates).map((template) => normalizeTemplate(template));

const sql = [
  "-- Generated by scripts/render-seed-sql.mjs.",
  "-- Review before applying to D1.",
  "BEGIN TRANSACTION;",
  ...sourceRows.map(renderSource),
  ...collectionRows.map(renderCollection),
  ...templateRows.map(renderTemplate),
  "COMMIT;",
  "",
].join("\n");

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, sql);

console.log(
  JSON.stringify(
    {
      output: outputArg,
      sources: sourceRows.length,
      collections: collectionRows.length,
      templates: templateRows.length,
    },
    null,
    2,
  ),
);

function normalizeSource(source) {
  const id = toId(required(source.id || source.name, "source.id"));
  const type = source.type === "local" ? "local" : "remote";
  return {
    id,
    name: stringValue(source.name, id),
    type,
    url: type === "remote" ? required(source.url, `sources.${id}.url`) : stringValue(source.url, ""),
    content: type === "local" ? required(source.content, `sources.${id}.content`) : stringValue(source.content, ""),
    enabled: source.enabled !== false,
    filters: expandFilters(source),
    meta: object(source.meta),
  };
}

function normalizeCollection(collection) {
  const id = toId(required(collection.id || collection.name, "collection.id"));
  return {
    id,
    name: stringValue(collection.name, id),
    sourceIds: array(collection.sourceIds),
    filters: expandFilters(collection),
    templateId: stringValue(collection.templateId, "acl4ssr-mihomo"),
    ignoreFailed: collection.ignoreFailed !== false,
    enabled: collection.enabled !== false,
    meta: object(collection.meta),
  };
}

function normalizeTemplate(template) {
  const id = toId(required(template.id || template.name, "template.id"));
  return {
    id,
    name: stringValue(template.name, id),
    target: stringValue(template.target, "mihomo"),
    config: object(template.config),
  };
}

function renderSource(source) {
  return [
    "INSERT INTO sources (id, name, type, url, content, enabled, filters_json, meta_json, created_at, updated_at)",
    `VALUES (${values([source.id, source.name, source.type, source.url, source.content, boolInt(source.enabled), json(source.filters), json(source.meta), now, now])})`,
    "ON CONFLICT(id) DO UPDATE SET",
    "  name = excluded.name,",
    "  type = excluded.type,",
    "  url = excluded.url,",
    "  content = excluded.content,",
    "  enabled = excluded.enabled,",
    "  filters_json = excluded.filters_json,",
    "  meta_json = excluded.meta_json,",
    "  updated_at = excluded.updated_at;",
  ].join("\n");
}

function renderCollection(collection) {
  return [
    "INSERT INTO collections (id, name, source_ids_json, filters_json, template_id, ignore_failed, enabled, meta_json, created_at, updated_at)",
    `VALUES (${values([collection.id, collection.name, json(collection.sourceIds), json(collection.filters), collection.templateId, boolInt(collection.ignoreFailed), boolInt(collection.enabled), json(collection.meta), now, now])})`,
    "ON CONFLICT(id) DO UPDATE SET",
    "  name = excluded.name,",
    "  source_ids_json = excluded.source_ids_json,",
    "  filters_json = excluded.filters_json,",
    "  template_id = excluded.template_id,",
    "  ignore_failed = excluded.ignore_failed,",
    "  enabled = excluded.enabled,",
    "  meta_json = excluded.meta_json,",
    "  updated_at = excluded.updated_at;",
  ].join("\n");
}

function renderTemplate(template) {
  return [
    "INSERT INTO templates (id, name, target, config_json, created_at, updated_at)",
    `VALUES (${values([template.id, template.name, template.target, json(template.config), now, now])})`,
    "ON CONFLICT(id) DO UPDATE SET",
    "  name = excluded.name,",
    "  target = excluded.target,",
    "  config_json = excluded.config_json,",
    "  updated_at = excluded.updated_at;",
  ].join("\n");
}

function values(items) {
  return items.map(sqlValue).join(", ");
}

function expandFilters(item) {
  return [
    ...array(item.filterPresetIds).flatMap((presetId) => filterPresets.get(String(presetId)) || []),
    ...array(item.filters),
  ];
}

function loadFilterPresets() {
  const raw = JSON.parse(readFileSync(resolve("config/rule-presets.json"), "utf8"));
  return new Map(
    array(raw.filters)
      .map((preset) => [stringValue(preset.id, ""), array(preset.filters)])
      .filter(([id]) => id),
  );
}

function sqlValue(value) {
  if (typeof value === "number") return String(value);
  return `'${String(value).replaceAll("'", "''")}'`;
}

function json(value) {
  return JSON.stringify(value);
}

function boolInt(value) {
  return value ? 1 : 0;
}

function required(value, label) {
  const text = stringValue(value, "");
  if (!text) throw new Error(`${label} is required`);
  return text;
}

function stringValue(value, fallback) {
  if (typeof value === "string") return value.trim();
  if (value === undefined || value === null) return fallback;
  return String(value).trim();
}

function array(value) {
  return Array.isArray(value) ? value : [];
}

function object(value) {
  return value && typeof value === "object" && !Array.isArray(value) ? value : {};
}

function toId(input) {
  return required(input, "id")
    .toLowerCase()
    .replace(/[^a-z0-9_-]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 64);
}
